let Module = {};

Module.array = [];
Module.color = [];

Module.RandomInRange = function (min, max) {
    return Math.floor(Math.random() * (max - min)) + min;
}
Module.InitArray = function () {
    for (let i = 0; i < 58; i++) {
        Module.array[i] = Module.RandomInRange(20, 365)
        Module.color[i] = CSS_COLOR_NAMES[Module.RandomInRange(0, CSS_COLOR_NAMES.length)]
    }
};


function init() {
    setInterval(BuildChart, 1000);
}

function BuildChart() {
    Module.InitArray();
    Module.barChart("Chart", 800, 400, Module.color, Module.array);
}

Module.barChart = function (canvas, width, height, color, data) {

    if (typeof canvas == "string") canvas = $(canvas);
    canvas.width = width;
    canvas.height = height;
    let context = canvas.getContext("2d");

    let max = Number.NEGATIVE_INFINITY;
    for (let i = 0; i < data.length; i++) {
        if (max < data[i]) max = data[i];
    }

    let scale = height / max;
    let barWidth = Math.floor(width / data.length);

    // создаем отдельный элемент диаграммы
    for (let i = 0; i < data.length; i++) {

        let barHeight = data[i] * scale,
            x = barWidth * i,
            y = height - barHeight;

        context.fillStyle = color[i];
        context.fillRect(x, y, barWidth - 2, barHeight);
    }
}


